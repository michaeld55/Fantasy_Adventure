package Resources;

public class Armour {

    private String name;
    private int armourPoint;

    public Armour(String name, int armourPoint) {
        this.name = name;
        this.armourPoint = armourPoint;
    }

    public String getName() {
        return name;
    }

    public int getArmourPoint() {
        return armourPoint;
    }
}
